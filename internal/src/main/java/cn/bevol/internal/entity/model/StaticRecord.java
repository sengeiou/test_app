package cn.bevol.internal.entity.model;



public class StaticRecord {
	
	private Integer id;
	private String mid;
	private String dataType;
	private String dataSource;
	private Integer state;
	private Integer isSeo;
	private Integer isAgain;
	private long createTime;
	private long updateTime;
	private Integer count;
	private String path;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getDataSource() {
		return dataSource;
	}
	public void setDataSource(String dataSource) {
		this.dataSource = dataSource;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public Integer getIsSeo() {
		return isSeo;
	}
	public void setIsSeo(Integer isSeo) {
		this.isSeo = isSeo;
	}
	public Integer getIsAgain() {
		return isAgain;
	}
	public void setIsAgain(Integer isAgain) {
		this.isAgain = isAgain;
	}
	public long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}
	public long getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(long updateTime) {
		this.updateTime = updateTime;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
	
}
